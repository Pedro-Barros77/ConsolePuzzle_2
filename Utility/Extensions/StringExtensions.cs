        public static char RemoveDiacritics(this char c)
        {
            foreign_characters.TryGetValue(c.ToString(), out string? value);
            
            return value == null ? c : value[0];
        }

        public static string RemoveDiacritics(this string s)
        {
            StringBuilder sb = new();

            foreach (char c in s)
            {
                sb.Append(c.RemoveDiacritics());
            }
            return sb.ToString();
        }

        public static bool HasBrackets(this string text, char b1, char b2)
        {
            return text[0] == b1 && text[^1] == b2;
        }

        public static string CompactJSON(this string json)
        {
            return Newtonsoft.Json.Linq.JObject.Parse(json).ToString(Newtonsoft.Json.Formatting.None);
        }
